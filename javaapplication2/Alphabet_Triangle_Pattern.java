public class Alphabet_Triangle_Pattern {
    public static void main(String[] args) {
        
     char asc = 97;
         for(int i=1;i<=5;i++)
        {  
        for(int j=1;j<=i;j++)
        {
            System.out.print(asc);    
        }
        asc++;
        System.out.println(" ");
        }}
    
}
